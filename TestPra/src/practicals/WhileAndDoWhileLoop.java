package practicals;

public class Practical4 {

	public static void main(String[] args) {
		
		int num = 0 ;
		
		// While Loop
		while (num <= 10) {
			System.out.println("Numbers for while loop : "+ num);
			num++;
		}
		
		int num2 = 0;
		
		//Do While Loop
		do {
			System.out.println("Number for doWhile loop : "+ num2);
			num2 ++;
		} 
		while (num2 <= 10);
		
	}
	
}
